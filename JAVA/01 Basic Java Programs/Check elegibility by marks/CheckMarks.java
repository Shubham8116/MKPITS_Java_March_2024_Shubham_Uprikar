//program to check student is pass or fail
//The time complexity of this code is O(1)
//The space complexity of this code is O(1)
public class CheckMarks {
    public void showResult(int marks)
    {
        if (marks>=50)
            System.out.println("you are pass in the exam");
        else
            System.out.println("you are fail in the exam");
    }
}
